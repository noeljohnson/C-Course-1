// to compare preincreamenting and post increamenting operations using addition

#include <stdio.h>

int main(void){
  
  int c = 5;
  printf("c = %d\n", c);
  printf("c = %d\n", c++);  
  printf("c = %d\n", c);
 
  c = 5;

  printf("c = %d\n", c);
  printf("c = %d\n", ++c);
  printf("c = %d\n", c);
}

